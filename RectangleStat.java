
public class RectangleStat {

	public static void main(String[] args) {
		//Stating variables
		int width = 15;
		int length = 18;
		//Formula for area
		int area = width * length;
		//Formula for perimeter
		int peri = 2*(width + length);
		//Result
		System.out.println("When one side is " + width + " and the other side is " + length +" the area is " + area +" and the perimeter is " + peri);

	}

}
