
public class CircleStat {

	public static void main(String[] args) {
		//Stating Variables
		double radius = 5;
		double pi = 3.14;
		//Formula for area
		double area = (pi)*(radius*radius);
		//Formula for circumference
		double circ = (2)*(pi)*(radius);
		//Result
		System.out.println("When the radius is " + radius + " the circumference is " + circ + " and the area is " + area);

	}

}
